import java.io.*;

public class Solution {
    public static void main(String[] args) throws Exception {


        for (int i = 0; i < 10; i++) {
            System.out.print(i + " ");
            if (i % 2 == 0) {
                continue;
            }
            System.out.println("");
        }

    }
}


